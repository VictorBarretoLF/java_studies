package com.example.demo.ai;

public class AIDemoThree {

    public static void main(String[] args) {
        for (int i = 0; i < 100; i ++) {
            if (i % 2 == 0) {
                System.out.println(i + " is even number");
            } else {
                System.out.println(i + " is odd number");
            }
        }
    }

}
